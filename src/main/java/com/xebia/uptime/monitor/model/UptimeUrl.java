package com.xebia.uptime.monitor.model;

public class UptimeUrl {

	private String url;

	public UptimeUrl() {

	}

	public UptimeUrl(final String url) {
		this.url = url;
	}

	public String getUrl() {
		return url;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + (url == null ? 0 : url.hashCode());
		return result;
	}

	@Override
	public boolean equals(final Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (getClass() != obj.getClass()) {
			return false;
		}
		final UptimeUrl other = (UptimeUrl) obj;
		if (url == null) {
			if (other.url != null) {
				return false;
			}
		} else if (!url.equals(other.url)) {
			return false;
		}
		return true;
	}

	@Override
	public String toString() {
		return "UptimeUrl [url=" + url + "]";
	}

}
